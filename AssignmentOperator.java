package IntroTopics;

public class AssignmentOperator {

    public static void main(String[] args) {

        //Assignment operators are used in java to assign values to variables.

        int age;
        age =18;

        int a=20;
        int val = age;
        System.out.println(val);

        val += a; //val =val +a
        System.out.println(val);

        val -= a; //val =val-a
        System.out.println(val);
    }
}
